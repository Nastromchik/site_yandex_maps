async function getCoords(address) {
    // –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã –æ–≥—Ä–∞–Ω–∏—á–∏–≤–∞—é—â–µ–≥–æ –ø—Ä—è–º–æ—É–≥–æ–ª—å–Ω–∏–∫–∞ –ú–æ—Å–∫–≤—ã (–ø—Ä–∏–º–µ—Ä–Ω—ã–µ –≥—Ä–∞–Ω–∏—Ü—ã –ú–ö–ê–î + –Ω–µ–º–Ω–æ–≥–æ –∑–∞–º–∫–∞–¥—å—è)
    // –§–æ—Ä–º–∞—Ç: "–¥–æ–ª–≥–æ—Ç–∞1,—à–∏—Ä–æ—Ç–∞1~–¥–æ–ª–≥–æ—Ç–∞2,—à–∏—Ä–æ—Ç–∞2" (—Å–ª–µ–≤–∞-—Å–Ω–∏–∑—É ~ —Å–ø—Ä–∞–≤–∞-—Å–≤–µ—Ä—Ö—É)
    const moscowBbox = "37.300000,55.500000~38.000000,56.050000"; 
    
    // –î–æ–±–∞–≤–ª—è–µ–º –ø–∞—Ä–∞–º–µ—Ç—Ä—ã &bbox=... –∏ &rspn=1
    const url = `https://geocode-maps.yandex.ru/1.x/?apikey=${API_KEY}&format=json&geocode=${encodeURIComponent(address)}&bbox=${moscowBbox}&rspn=1`;
    
    try {
        const res = await fetch(url);
        const data = await res.json();
        
        // –ü—Ä–æ–≤–µ—Ä—è–µ–º, –Ω–∞—à–µ–ª –ª–∏ –Ø–Ω–¥–µ–∫—Å —á—Ç–æ-—Ç–æ –≤–Ω—É—Ç—Ä–∏ —ç—Ç–æ–π –æ–±–ª–∞—Å—Ç–∏
        const featureMember = data.response.GeoObjectCollection.featureMember;
        
        if (!featureMember || featureMember.length === 0) {
            // –ï—Å–ª–∏ –≤–∫–ª—é—á–µ–Ω rspn=1 –∏ –∞–¥—Ä–µ—Å –≤–Ω–µ –ú–æ—Å–∫–≤—ã, –≤–µ—Ä–Ω–µ—Ç—Å—è –ø—É—Å—Ç–æ–π —Å–ø–∏—Å–æ–∫
            return null;
        }

        // –ü–∞—Ä—Å–∏–º –æ—Ç–≤–µ—Ç: "lon lat" -> { lat, lon }
        const pos = featureMember[0].GeoObject.Point.pos;
        const [lon, lat] = pos.split(' ').map(Number);
        return { lat, lon };
    } catch (e) {
        console.error("–û—à–∏–±–∫–∞ –≥–µ–æ–∫–æ–¥–∏–Ω–≥–∞:", e.message);
        return null;
    }
}

// –ü–æ—Å—Ç—Ä–æ–µ–Ω–∏–µ –º–∞—Ä—à—Ä—É—Ç–∞ (OSRM - –±–µ—Å–ø–ª–∞—Ç–Ω—ã–π —Å–µ—Ä–≤–∏—Å –º–∞—Ä—à—Ä—É—Ç–∏–∑–∞—Ü–∏–∏)
async function getRoute(start, end) {
    
    // OSRM —Ç—Ä–µ–±—É–µ—Ç –ø–æ—Ä—è–¥–æ–∫: lon,lat
    const url = `http://router.project-osrm.org/route/v1/driving/${start.lon},${start.lat};${end.lon},${end.lat}?overview=false`;
    try {
        const res = await fetch(url);
        const data = await res.json();
        if (data.code !== 'Ok') return null;
        return {
            dist: (data.routes[0].distance / 1000).toFixed(1), // –∫–º
            time: Math.round((data.routes[0].duration / 60)* TRAFF_COEFF)      // –º–∏–Ω—É—Ç—ã * –∫–æ—ç—Ñ—Ñ —Ä–µ–∞–ª—å–Ω–æ—Å—Ç–∏
        };
    } catch (e) {
        return null;
    }
}

// –†–∞—Å—á–µ—Ç —Ä–∞—Å—Å—Ç–æ—è–Ω–∏—è –ø–æ –ø—Ä—è–º–æ–π (—Ñ–æ—Ä–º—É–ª–∞ –•–∞–≤–µ—Ä—Å–∏–Ω–∞)
function getDirectDist(c1, c2) {
    const R = 6371e3; // –†–∞–¥–∏—É—Å –ó–µ–º–ª–∏
    const toRad = x => x * Math.PI / 180;
    const dLat = toRad(c2.lat - c1.lat);
    const dLon = toRad(c2.lon - c1.lon);
    const a = Math.sin(dLat/2) * Math.sin(dLat/2) +
              Math.cos(toRad(c1.lat)) * Math.cos(toRad(c2.lat)) *
              Math.sin(dLon/2) * Math.sin(dLon/2);
    return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
}

