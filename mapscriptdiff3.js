const delay = ms => new Promise(r => setTimeout(r, ms));

// –ò–°–ü–†–ê–í–õ–ï–ù–ù–ê–Ø –§–£–ù–ö–¶–ò–Ø (–ò—Å–ø–æ–ª—å–∑—É–µ—Ç Photon API –≤–º–µ—Å—Ç–æ Nominatim)
async function getCoords(address) {
    // Photon –Ω–µ —Ç—Ä–µ–±—É–µ—Ç User-Agent –∏ —Ä–∞–±–æ—Ç–∞–µ—Ç —Å—Ç–∞–±–∏–ª—å–Ω–µ–µ
    // –î–æ–±–∞–≤–ª—è–µ–º bbox, —á—Ç–æ–±—ã –∏—Å–∫–∞—Ç—å –ø—Ä–∏–æ—Ä–∏—Ç–µ—Ç–Ω–æ –≤–æ–∫—Ä—É–≥ –ú–æ—Å–∫–≤—ã (–ø—Ä–∏–º–µ—Ä–Ω—ã–µ –∫–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã)
    const url = `https://photon.komoot.io/api/?q=${encodeURIComponent(address)}&limit=1&bbox=36.8,55.1,38.2,56.4`;
    
    try {
        const res = await fetch(url);
        if (!res.ok) throw new Error(`HTTP Error ${res.status}`);

        const data = await res.json();
        
        // Photon –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç GeoJSON. –ö–æ–æ—Ä–¥–∏–Ω–∞—Ç—ã —Ç–∞–º –≤ —Ñ–æ—Ä–º–∞—Ç–µ [lon, lat]
        if (data.features && data.features.length > 0) {
            const [lon, lat] = data.features[0].geometry.coordinates;
            return { lat, lon };
        } else {
            return null;
        }
    } catch (e) {
        console.error(`–û—à–∏–±–∫–∞ (${address}):`, e.message);
        return null;
    }
}

// –ü–æ—Å—Ç—Ä–æ–µ–Ω–∏–µ –º–∞—Ä—à—Ä—É—Ç–∞ (OSRM)
async function getRoute(start, end) {
    const url = `http://router.project-osrm.org/route/v1/driving/${start.lon},${start.lat};${end.lon},${end.lat}?overview=false`;
    try {
        const res = await fetch(url);
        const data = await res.json();
        if (data.code !== 'Ok') return null;
        return {
            dist: data.routes[0].distance / 1000,
            time: (data.routes[0].duration / 60) * TRAFF_COEFF
        };
    } catch (e) { return null; }
}

// –†–∞—Å—Å—Ç–æ—è–Ω–∏–µ –ø–æ –ø—Ä—è–º–æ–π
function getDirectDist(c1, c2) {
    const R = 6371e3; 
    const toRad = x => x * Math.PI / 180;
    const dLat = toRad(c2.lat - c1.lat);
    const dLon = toRad(c2.lon - c1.lon);
    const a = Math.sin(dLat/2) * Math.sin(dLat/2) +
              Math.cos(toRad(c1.lat)) * Math.cos(toRad(c2.lat)) *
              Math.sin(dLon/2) * Math.sin(dLon/2);
    return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1-a));
}

