        console.error(`   –°–æ–æ–±—â–µ–Ω–∏–µ: ${e.message}`);
        if (e.cause) console.error(`   –ü—Ä–∏—á–∏–Ω–∞ (cause):`, e.cause);
        
        // –ü–æ–¥—Å–∫–∞–∑–∫–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—é –Ω–∞ –æ—Å–Ω–æ–≤–µ –∫–æ–¥–∞ –æ—à–∏–±–∫–∏
